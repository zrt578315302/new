 $('#top1').mouseenter(function(){
      
       
       $('.hideya').slideToggle(100);
       $('.hideya').addClass('topa1');
       $('#top1').addClass('aiyo');
       
     
   })
    $('#top1').mouseleave(function(){
      
       
       $('.hideya').slideToggle(100);
       $('.hideya').removeClass('topa1');
       $('#top1').removeClass('aiyo');
        
     
   })
